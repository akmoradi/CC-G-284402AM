package exercises;

public class Primitive {

	public static void main(String[] args) {
		
		// Datentyp: Integer
		int i = 42;
		IO.writeln("i = " + i);
		
		// Datentyp: Byte
		byte b = 10;
		IO.writeln("b = " +b);
		
		// Datentyp: short
		short s = 20;
		IO.writeln("s = " + s);
		
		// Datentyp: long
		long l = 100000L;
		IO.writeln("l = " + l);
		
		// Datentyp: Float
		float f = 3.14f;
		IO.writeln("f = " + f);
		
		// Datentyp: Double
		double d = 3.14357892345;
		IO.writeln("d = " + d);
		
		
		// Datentyp: char
		char c = 'A';
		IO.writeln("c = " + c);
		
		// Datentyp: Boolean
		boolean bool = true;
		IO.writeln("bool = " + bool);
		
	}

}

