package exercises;

public class DoubleOperationen {

	public static void main(String[] args) {
		double i = 7.5;
		IO.writeln("i = " + i);

		double j = 2.3;
		IO.writeln("j = " + j);
		
		double add = i + j;
		IO.writeln("i + j = " + add);

		double sub = i - j;
		IO.writeln("i - j = " + sub);
		
		double div = i / j;
		IO.writeln("i / j = " + div);
		

	}

}
